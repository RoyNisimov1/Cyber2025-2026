public class NumCount {

    private int count;
    private int num;
    public NumCount(int num, int count){
        this.setNum(num);
        this.setCount(count);
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }
}
